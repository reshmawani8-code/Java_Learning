package Testpckg1;

public class SwapNumber2 {
    public static void main(String[] args) {
        int a= 12;
        int b= 23;
        System.out.println("Before swapping numbers are...."+a+"  "+b);

        int t=a;//t=12
        a=b;//a=23
        b=t;//b=12
        System.out.println("Values after swapping   "+a+"  "+b);


    }
}
